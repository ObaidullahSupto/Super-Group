    <div class="container">
        <div class="row">
            <div class="col-sm-3">
                    <p class="module-text--two-col__headline headline headline--small rte-color--orange dash dash--after dash--orange text-align--right no-padding--top">
                        Tata Power Solar, India’s largest integrated solar company, and Huawei Technologies, a world leader in inverter technology, have established new contracts and partnerships aiming to support Better Energy’s vision and ambitious growth plans to increase installed capacity up to 1 GWh by 2020.
                    </p>
            </div>
            <div class="col-sm-6">
                <section class="module-text
            position-horizontal--left position-vertical--bottom
         
         
         fade-in-up">

    <div class="text-area ">
        <p>Management from Huawei and Tata travelled to Better Energy’s new offices at Resilience House in Vejle, Denmark, to sign new agreements. Resilience House is a newly opened innovation and development complex for future resilient environmental solutions and formed the perfect backdrop for the event.</p>
<p>The partnership agreements signed at Resilience House demonstrate joint commitments to both achieving business goals and accelerating the pace of the energy transition.</p>
<p>“Better Energy is currently exploring over 2 GWh in opportunities, and delivering on our agenda requires the combined strengths of several partners,” explains Mikkel Dau Jacobsen, EVP Technology &amp; Solutions.</p>
<p> </p>
<p><strong>Why Tata Power Solar?</strong></p>
<p>Better Energy has long been looking for a partnership with a solar module supplier.</p>
<p>“We quickly found common ground with Tata and many areas of collaboration beyond the supply of solar modules. They have tremendous experience with EPC (engineering, procurement and construction) and technical design, and they bring a unique set of knowledge of new markets which complements our own. We were fortunate to gain this new connection through Tata Steel who supplies steel for our projects,” explains Mikkel Dau Jacobsen.</p>
<p>Tata Power Solar has expanded its global market reach by shipping over 1GW solar modules in the last four years alone. The partnership with Better Energy is special because it demonstrates their shared commitment to quality and customer satisfaction. This partnership further strengthens Tata Power Solar’s presence in the European market.</p>
<p>Mr. Ashish Khanna, Executive Director &amp; CEO Tata Power Solar, said, “It is a privilege to be a partner of Better Energy and deliver world-class solar solutions for European solar projects. The partnership is testimony of our manufacturing excellence and high-quality standards which is our company’s forte for the last 27 years. We are confident that our close collaboration, value proposition and quality of products will pave the way for enhanced business opportunities. We are also appreciative of Tata Steel Denmark for the collaborative efforts.”</p>
<p>Tata Power Solar is a pioneer in the Indian solar manufacturing space, has been known for its quality modules for 27 years and has one of the lowest warranty returns in the industry. They are rated a tier-1 module manufacturer by BNEF (Bloomberg New Energy Finance) and have shipped 1 GW modules to over 30 countries – the first Indian company to boast this milestone.</p>
<p><img style="width: 500px; height: 363.02083333333337px;" src="../../media/1164/56629c65.jpg?width=500&amp;height=363.02083333333337" alt="" data-udi="umb://media/7db5368333fc4eebb165cd5c157d50f9" /><img style="width: 0px; height: 0px;" src="../../nothing.html" alt="" data-udi="umb://media/7db5368333fc4eebb165cd5c157d50f9" /></p>
<p> </p>
<p><strong>Huawei inverters match Danish park construction</strong></p>
<p>The capacity to scale up to required volume was just one of the considerations when deciding to strengthen the relationship with Huawei.</p>
<p>“We have been using Huawei inverters for a while now on several projects. Here we can talk about state-of-the-art technology and an impressive number of features and functionalities for the price. The inverters function really well, but they also have a specific technical design that fits to the way we build parks here in Denmark,” says Mikkel Dau Jacobsen.</p>
<p>“Better Energy is an important partner for Huawei FusionSolar. The cooperation between us is a step further towards the advancements of solar technology and for the welfare of human beings. Huawei is also a customer-centric company. We will collaborate together with Better Energy to achieve their goals for a win-win future through advanced technology and continuous innovation,” Alison Finch, Chief Marketing Officer of Huawei FusionSolar Europe says.<br /><br /></p>
<p><strong>Moving from transaction to transformation</strong></p>
<p>“Supplier contracts reduce costs, and collaborations coordinate efforts. But these are still terms and transactions,” explains Mikkel Dau Jacobsen. “Partnerships are more about finding fundamental common principles and interests, finding shared values and taking the time to understand each other’s aims and motivations. This is the only way to achieve real scale and impact. Real transformation.”<br /><br /></p>
<p><strong>About Tata Power Solar</strong></p>
<p>Tata Power Solar, with 27 years of deep domain expertise, is one of the pioneering solar manufacturers in the world and India’s largest specialised EPC player. Founded in 1989, the company was originally formed as a joint venture between Tata Power and British Petroleum Solar (BP Solar). As a pioneer and market leader in the solar space, headquartered in Bangalore, Tata Power Solar now operates independently as a wholly owned subsidiary of Tata Power.</p>
<p>As one of the largest solar manufacturers in India, Tata Power Solar operates a world-class manufacturing unit in Bangalore, with a production capacity of 400 MW of modules and 300 MW of cells. It has completed more than 605 MW of ground-mount utility scale and 150 MW of rooftop and distributed generation projects across the country till date. It also offers a diverse line of solar solutions for both urban and rural markets – these include rooftop solutions, solar pumps and power packs among others. Tata Power Solar is committed to enabling solar everywhere and aims to provide energy access to millions of people across the country via its integrated solar solutions. For more information visit: www.tatapowersolar.com<br /><br /></p>
<p><strong>About Tata Steel’s European operations</strong></p>
<p>Tata Steel is one of Europe’s leading steel producers, with steelmaking in the UK and Netherlands, and manufacturing plants across Europe. The company supplies high-quality steel products to the most demanding markets, including construction and infrastructure, automotive, packaging and engineering. Tata Steel works with customers to develop new steel products that give them a competitive edge. The combined Tata Steel group is one of the top global steel companies, with an annual steel capacity of 27.5 million tonnes and almost 74,000 employees across five continents. The group’s revenue in the year ending March 2017 was $18.1 billion.<br /><br /></p>
