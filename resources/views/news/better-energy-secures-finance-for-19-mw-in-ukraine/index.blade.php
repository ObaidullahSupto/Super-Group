<section class="module-text module-text--two-col fade-in-up">

    <div class="container">
        <div class="row">
            <div class="col-sm-3">
                    <p class="module-text--two-col__headline headline headline--small rte-color--orange dash dash--after dash--orange text-align--right no-padding--top">
                        NEFCO and Danish company Better Energy have signed a loan agreement for the construction of Phase 1 of a 19 MW solar park in Western Ukraine. The total investment amounts to EUR 6.6 million.
                    </p>
            </div>
            <div class="col-sm-6">
                <section class="module-text
            position-horizontal--left position-vertical--bottom
         
         
         fade-in-up">

    <div class="text-area ">
        <p>The site is ready and the grid connection is prepared, and now a financing agreement has been signed by NEFCO (Nordic Environment Finance Corporation) and Better Energy for Phase I of a 19 MW Better Energy solar PV park located in the Zhytomyr region in western Ukraine. The new park will be constructed in the southern part of Zhytomir oblast, east of Berdychiv, and the project is set to start delivering green electricity to the Ukrainian grid in 2018. The plan is to expand the Berdychiv solar park to 19 MW by the end of 2018.</p>
<p><span>NEFCO and Better Energy will each invest EUR 3.3 million in Phase I. The 6.4 MW park will consist of 19,700 solar panels and is expected to produce 6,660 MWh green electricity per year, providing more than 1000 homes with green electricity. This will reduce CO2 emissions from the electricity production in Ukraine by approximately 5,500 tonnes annually. </span><span> </span></p>
<p><span>“We are very content to be able to cooperate with Better Energy in Ukraine, as the Danish company has broad experience in both financing and constructing solar plants,” says Senior Investment Manager Bo Nyhus from NEFCO.</span></p>
<p>“There is enormous potential in Ukraine for renewable energy. NEFCO’s focus is on achieving positive environmental results and is a good cooperation partner for us. Better Energy can provide a scalable, sustainable approach to energy access and support Ukraine in transforming its energy sector away from fossil fuels and nuclear,” explains EVP Business Development &amp; Public Relations Michael Vater of Better Energy.</p>
<p> </p>
<p><strong>Accelerating progress in Ukraine</strong></p>
<p>Better Energy considers this to be a pilot project. Hopefully, it will pave the way for future projects and act as a catalyst for accelerating market adoption of solar energy in the country. Better Energy’s Michael Vater has over 10 years of previous experience with manufacturing in the Ukraine.</p>
<p>“We are talking about a workforce of people who are skilled, productive, and committed. By transitioning to a cleaner power system, Ukraine can increase opportunities for local manufacturing and build a strong economy,” says Michael Vater.</p>
<p>“To achieve impact on a significant scale, we work to establish ourselves in markets where we can play a pivotal role in the development of new solar capacity. Ukraine is one of those markets where we can really make an impact.”</p>
<p> </p>
<p><strong><span>There is much to win</span></strong></p>
<p><span>Ukraine has initiated extensive reforms, which in the long term will strengthen integration with the EU and create economic and democratic improvements for the population. One of the best ways to ensure progress is through private investment and special investment in renewable energy and energy efficiency. And there is much to win for Ukraine, which is Europe's most energy-intensive country.</span></p>
<p><span>In 2014, Ukraine set a target that 11 percent of its energy would come from renewable sources by 2020, and currently several investments within the renewable energy field are being initiated in the country. Key areas of renewable energy sources in Ukraine are wind, solar, hydro and biomass.</span></p>
<p><span> </span></p>
<p><strong>Powering and empowering</strong></p>
<p>“Our ambition is to make affordable solar energy for the benefit of the Ukrainian people and energy consumers. The successful implementation of this pilot project will open up for many more investments in Ukraine and the Zhytomyr Region, and help to bring long-term prosperity and clean energy,” says EVP Markets &amp; Project Finance Mark Augustenborg Ødum from Better Energy.</p>
<p>“Solar energy is a means to powering a country and empowering its people.”</p>
<p><em><span> </span></em></p>
<p><strong><em><span>More about NEFCO</span></em></strong></p>
<p><em><span>NEFCO is an international financial institution (IFI) established in 1990 by the five Nordic countries: Denmark, Finland, Iceland, Norway and Sweden. NEFCO finances and implements projects related to climate change prevention and green growth investments across the world. All of NEFCO’s activities aim to generate positive environmental impact. This is NEFCO’s second solar energy project to be implemented in Ukraine this year. Additionally, NEFCO has financed several other renewable energy projects in the country. The current project portfolio in Ukraine comprises over 130 projects at various stages.</span></em><span> </span></p>
