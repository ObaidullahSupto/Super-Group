                  <p
                     class="module-text--two-col__headline headline headline--small rte-color--orange dash dash--after dash--orange text-align--right no-padding--top">
                     With new green energy capacity in Northern Europe and a historic milestone in the transition to a
                     sustainable energy system in Denmark, Better Energy delivered solid results in 2018.
                  </p>
               </div>
               <div class="col-sm-6">
                  <section class="module-text
                        position-horizontal--left position-vertical--center
                        fade-in-up">
                     <div class="text-area ">
                        <p>Better Energy continued to drive down the cost of solar energy in 2018 and delivered a
                           positive measurable difference in the green transition with deployment of new solar energy
                           capacity.</p>
                        <p>Strong growth continued and financial results for the year were positive. Revenue reached DKK
                           424 million and operating profit increased to DKK 101 million.</p>
                        <p>The company also scaled up the export of its technology and solutions which accounted for
                           almost half of the total revenue. Strategic focus for export is on Northern Europe and the
                           Nord Pool power markets where the company can best utilise its integrated value chain.</p>
                        <p>“We are satisfied with the results after another busy and productive year. Financial growth
                           is essential for Better Energy to achieve scale and impact in markets and drive the
                           transition to renewable energy sources,” says Better Energy CEO Rasmus Lildholdt Kjær.</p>
                        <p><strong>A historic milestone in Denmark with subsidy-free solar power</strong></p>
                        <p>In 2018 Better Energy built up a pipeline of projects under development amounting to more
                           than 1 GW ready to build in the coming years. In addition, the company received orders for
                           more than 150 MW solar energy capacity to be delivered and constructed in 2019 and 2020.</p>
                        <p>Better Energy reached a historic milestone in the transition to a sustainable energy system
                           in Denmark with the first Danish subsidy-free, large-scale solar power plant. The solar power
                           plant was ordered in 2018 and will be constructed in 2020. Additionality, the idea that new
                           renewable energy capacity gets built and added to the grid, was an important aspect of the
                           agreement.</p>
                        <p>“Climate change is the challenge of our generation. We all need to work together in the
                           transition to a sustainable energy system. Better Energy will continue to focus on
                           large-scale solar energy capacity where we can make the greatest difference in terms of
                           impact and affordable prices,” says Better Energy CEO Rasmus Lildholdt Kjær, and concludes:
                        </p>
                        <p>“With affordable prices on green energy, we are pioneering a new way for corporations to
                           access green energy in Denmark and Northern Europe, while also adding new green energy to the
                           grid. Corporations can now choose how they purchase clean energy. They will play a major role
                           in the development of a clean energy economy.”</p>
