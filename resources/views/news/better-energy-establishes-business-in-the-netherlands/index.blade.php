                    <p class="module-text--two-col__headline headline headline--small rte-color--orange dash dash--after dash--orange text-align--right no-padding--top">
                        Amsterdam will be home base for Better Energy Nederland B.V., providing the Dutch market with a full-service suite of solar PV services, including design, planning, development, financing construction, asset management and ongoing operation and maintenance.
                    </p>
            </div>
            <div class="col-sm-6">
                <section class="module-text
            position-horizontal--left position-vertical--bottom
         
         
         fade-in-up">

    <div class="text-area ">
        <p>Better Energy has established a new business in the Netherlands together with the management team from Adamant Renewables in Amsterdam.</p>
<p>There is great potential for commercial PV installations in the Netherlands, and there is high demand for experienced full-service partners who can realize large-scale projects efficiently, with low cost of financing and installation, and high technical standards. This is precisely how Better Energy can support the Dutch energy transition.</p>
<p>Better Energy Nederland will be active in the rooftop market, utility-scale ground-mounted projects, as well as floating solar and carport solutions.</p>
<p>“We consider the Netherlands a new home market. This represents a concerted effort and a serious commitment to advance affordable and sustainable solar energy in the Netherlands,” says EVP Markets &amp; Project Finance Mark Augustenborg Ødum of Better Energy.</p>
<p>“We have found a Dutch management team with incredible drive, dedication, and industry expertise that we believe can carve out a leading position in a fast-growing market. With a focused, local team we can react quickly to national demand and market conditions.”<br /><br /></p>
<p><strong>Better Energy Nederland</strong></p>
<p>The Dutch management team for Better Energy Nederland comprises Managing Director Pieter Bijlsma, Development Director Ismael Bena and Project Development Manager Erik-Jan van der Linden.</p>
<p>The Dutch team have more than 30 years of combined experience in the development, construction, financing and asset management of renewable energy assets. They have worked for a broad number of companies within the solar energy value chain, including developers, module manufacturers, EPCs (engineering, procurement and construction companies) and asset management companies.</p>
<p>“Adamant Renewables is excited to have secured such a strong and reliable partner as Better Energy for joint solar operations in the Netherlands. With Better Energy, we will be able to bring the full spectrum of services to the market: from greenfield development, project acquisition, procurement, engineering and construction to financing and long-term operation and maintenance (O&amp;M),” says Managing Director Pieter Bijlsma from Better Energy Nederland.</p>
<p>“It is with an open mindset, creativity and determination for continuous improvements we bring clean energy forwards, in our combined efforts to deliver better energy, each day, to all our stakeholders,” adds Development Director Ismael Bena, Adamant Renewables.</p>
